    private boolean check (JTextField usuario, JPasswordField contrasena, JTextField nombres, JTextField apellidoP, JTextField apellidoM, JTextField correo ){
        boolean result = false;
        if (usuario.getText().isEmpty()|| String.valueOf(contrasena.getPassword()).isEmpty()||nombres.getText().isEmpty()||apellidoP.getText().isEmpty()||correo.getText().isEmpty()) {
            result = true;
        }
        return result;
        
    }
    private void jLabel7MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel7MousePressed
        try {
            
            conn = DriverManager.getConnection(url);
            if (check(usuario, contrasena, nombres, apellidoP, apellidoM, correo)==false) {
                if (conn!=null) {
                    PreparedStatement stmt = conn.prepareStatement("SELECT * FROM usuarios WHERE usuario = ?");
                    stmt.setString(1, usuario.getText());
                    ResultSet rs = stmt.executeQuery();
                if (!rs.next()) {
                        PreparedStatement stmt2 = conn.prepareStatement("INSERT INTO usuarios (usuario, contrasena, nombres, apellido_paterno, apellido_materno, correo) VALUES (?,?,?,?,?,?)");
                        stmt2.setString(1, usuario.getText());
                        stmt2.setString(2, String.valueOf(contrasena.getPassword()));
                        stmt2.setString(3, nombres.getText());
                        stmt2.setString(4, apellidoP.getText());
                        stmt2.setString(5, apellidoM.getText());
                        stmt2.setString(6, correo.getText());
                        stmt2.executeUpdate();
                        JOptionPane.showMessageDialog(null, "Registro exitoso", "Registro", 0);

                    }
                else{
                    JOptionPane.showMessageDialog(null, "El registro ya existe", "Registro", 0);

                }
                
                }
            } else{
                JOptionPane.showMessageDialog(null, "Rellena los campos restantes", "Registro", 0);
            }
            
            
        } catch (SQLException ex) {
            Logger.getLogger(registrarPanel.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            try {
                if (conn!= null) {
                    conn.close();
                }
            } catch (Exception e) {
            }
        }
    }//GEN-LAST:event_jLabel7MousePressed

